package C10_Defining_Classes_Exercises.C7_Google;

public class Parents {
   private String parentsname;
   private String parenbirth;

    public Parents(String parentsname, String parenbirth) {
        this.parentsname = parentsname;
        this.parenbirth = parenbirth;
    }

    public String getParentsname() {
        return parentsname;
    }

    public String getParenbirth() {
        return parenbirth;
    }
}
